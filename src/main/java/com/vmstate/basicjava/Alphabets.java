package com.vmstate.basicjava;

/**
 * This example demonstrates printing alphabets using for loop
 * 
 * @author Rajesh Abraham
 *
 */

public class Alphabets
{
	
	   public static void main(String args[])
	   {
	      char ch;
	 
	      for( ch = 'a' ; ch <= 'z' ; ch++ )
	         System.out.println(ch);
	   }
}
