package com.vmstate.loops;
/**
 * This example demonstrates simple usage of Do While loop
 * 
 * @author Rajesh Abraham
 *
 */
public class DoWhileLoop2 
{

	   public static void main(String args[])
	   {
	      char c = 'a';
	 	  	 
	      do	{
	    	  		System.out.println(c);
	    	  		c++;
			    
	            } while (c < 'z');
	   }
}
