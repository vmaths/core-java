package com.vmstate.loops;
/**
 * This example demonstrates simple usage of while loop
 * while loop is a control structure that allows to repeat a task a certain number of times
 * @author Rajesh Abraham
 */
public class SampleWhileLoop1 
{
	
		   public static void main(String args[])
		   {
		      int x = 1;// initialization  of variables
		      while( x <=10 ) // condition-boolean expression
		      {
		         System.out.println("value of x : " + x ); // body of loop, statements to execute
		         x++;
		      }
		   }
}

