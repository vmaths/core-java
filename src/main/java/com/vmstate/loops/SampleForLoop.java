package com.vmstate.loops;
/**
 * This example demonstrates simple usage of For loop
 * Using for loop to repeat instruction execution
 * @author Rajesh Abraham
 */
public class SampleForLoop 
{
	public static void main(String[] arguments)
		  {
		    int c; 
		   	//(initialization of variables;conditions to test; increment or decrement of variables) 
		    for (c = 1; c <= 10; c++)
		    {
		      System.out.println(c);// statements to execute, that is body of for loop 
		    }
		  }  

}

		
